int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
	{
		ft_putchar(str[count]);
		count++;
	}
}

int		main(int argc, char *argv[])
{
	ft_putstr(argv[argc - 1]);
	ft_putchar('\n');
	return (0);
}
